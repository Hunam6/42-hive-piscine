#include "ft_list.h"

t_list	*ft_list_at(t_list *begin_list, unsigned int nbr)
{
	unsigned int	i;
	t_list			*node;

	if (nbr < 0 || begin_list == NULL)
		return (NULL);
	i = 0;
	node = begin_list;
	while (i < nbr)
	{
		node = node->next;
		i++;
	}
	return (node);
}
